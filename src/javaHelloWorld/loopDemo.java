package javaHelloWorld;

public class loopDemo {

	public static void main(String[] args) {
		//For
		for(int i=1;i<=5;i++) {
			System.out.println(i);
		}
		
		System.out.println("Döngü Bitti");
		
		for(int i=1;i<=10;i=i+2) {
			System.out.println(i);
		}
		
		System.out.println("Döngü Bitti");
	}

}
